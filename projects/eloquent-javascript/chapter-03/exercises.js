// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(numOne, numTwo) {
  if (numOne > numTwo) {
    	return numTwo;
    } else {
    	return numOne;
    }
}


////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  if (num < 0) {
    	num = -num;
    }
	if (num === 0) {
    	return true;
    } else if (num === 1) {
   		return false;
   	} else {
   		num -= 2;
    	return isEven(num);
   	}
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChar(string, char) {
  let count = 0;
	for (let i = 0; i < string.length; i++) {
    	if (string[i] === char) {
        	count++;
        }
    }
	return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  let count = 0;
	for (let i = 0; i < string.length; i++) {
    	if (string[i] === 'B') {
        	count++;
        }
    }
	return count;
}

